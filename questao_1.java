public class questao_1 {

    public static void main(String[] args) {
        int indice = 13;
        long soma = 0;

        for (int k = 1; k < indice; k++) {
            soma = soma + k;
        }
        System.out.println("O valor final da variável SOMA será de: " + soma);
        //o valor total é de 78
    }
}